    private void lblRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblRegistrarMouseClicked
        if (lblRegistrar.isEnabled()) {
            SimpleDateFormat formatoFecha = new SimpleDateFormat("YYYY-MM-dd");
            String fechaNacimiento = "";
            try {
                fechaNacimiento = formatoFecha.format(jDateFechaNacimiento.getDate());
            } catch (NullPointerException e) {
                JOptionPane.showMessageDialog(this, "Seleccione la fecha de nacimiento");
            }

            String primerNombre = txtPrimerNombre.getText();
            String segundoNombre = txtSegundoNombre.getText();
            String primerApellido = txtPrimerApellido.getText();
            String segundoApellido = txtSegundoApellido.getText();
            String direccion = txtDireccion.getText();
            String celular = txtCelular.getText();
            String cedula = txtCedula.getText();

            if (txtCedula.getText().length() != 8 || !txtCedula.getText().matches("[0-9]+")) {
                JOptionPane.showMessageDialog(this, "Cédula incorrecta");
            } else if (txtCelular.getText().length() != 9 || !txtCelular.getText().matches("[0-9]+")) {
                JOptionPane.showMessageDialog(this, "Celular incorrecto");
            } else if (primerNombre.isEmpty() || primerApellido.isEmpty() || direccion.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Verifique los campos");
            } else {
                celular = txtCelular.getText();
                cedula = txtCedula.getText();
                Empleado empleado = new Empleado(primerNombre, segundoNombre, primerApellido, segundoApellido, fechaNacimiento, cedula, direccion, celular);
                try {
                    if (Sistema.getInstance().registrarEmpleado(empleado)) {
                        JOptionPane.showMessageDialog(this, "¡Se registró correctamente!");
                    } else {
                        JOptionPane.showMessageDialog(this, "Error! No se puedo registrar.");
                    }
                } catch (ClassNotFoundException ex) {
                    java.util.logging.Logger.getLogger(Empleados.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Empleados.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    java.util.logging.Logger.getLogger(Empleados.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                }
            }
            try {
                tbeListado_Empleados.setModel(Sistema.getInstance().mostrarListadoEmpleados());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Empleados.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Empleados.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Empleados.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_lblRegistrarMouseClicked

