    private void lblModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblModificarMouseClicked

        int filaSeleccionada = tbeListado_Usuarios.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione el usuario que desea modificar...");
        } else {
            btnCrear.setEnabled(false);
            lblCrear.setEnabled(false);
            btnCrear.setBackground(new Color(255, 176, 199));
            btnActualizar.setEnabled(true);
            lblActualizar.setEnabled(true);
            btnActualizar.setBackground(new Color(255, 77, 123));
            txtNuevo_Usuario.setText(String.valueOf(tbeListado_Usuarios.getValueAt(filaSeleccionada, 2)));
            txtContrasenia.setText("");
            txtConfirmarContrasenia.setText("");
            txtIdEmpleado.setText(String.valueOf(tbeListado_Usuarios.getValueAt(filaSeleccionada, 1)));
            txtIdEmpleado.setEnabled(false);
            if (String.valueOf(tbeListado_Usuarios.getValueAt(filaSeleccionada, 3)).equalsIgnoreCase("Administrador")) {
                cboTipo_Usuario.setSelectedIndex(1);
            } else if (String.valueOf(tbeListado_Usuarios.getValueAt(filaSeleccionada, 3)).equalsIgnoreCase("Vendedor")) {
                cboTipo_Usuario.setSelectedIndex(2);
            }
            tbeListado_Usuarios.setEnabled(false);
        }
    }//GEN-LAST:event_lblModificarMouseClicked

