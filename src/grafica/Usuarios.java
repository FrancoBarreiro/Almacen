    private void lblEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEliminarMouseClicked
        int filaSeleccionada = tbeListado_Usuarios.getSelectedRow();
        String idUsuario = null;
        String nombreUsuario = null;
        String tipoUsuario = null;

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione el usuario que desea eliminar...");
        } else {
            idUsuario = String.valueOf(tbeListado_Usuarios.getValueAt(filaSeleccionada, 0));
            nombreUsuario = (String.valueOf(tbeListado_Usuarios.getValueAt(filaSeleccionada, 2)));
            tipoUsuario = (String.valueOf(tbeListado_Usuarios.getValueAt(filaSeleccionada, 3)));
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Desea eliminar el usuario: " + nombreUsuario + ", Tipo: " + tipoUsuario + "?", "Confirmación de eliminación...", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirmacion == 0) {
                try {
                    if (Sistema.getInstance().eliminarUsuario(idUsuario)) {
                        JOptionPane.showMessageDialog(this, "Se ha elmininado correctamente.");
                    } else {
                        JOptionPane.showMessageDialog(this, "Se ha producido un error, intente nuevamente.");
                    }
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    tbeListado_Usuarios.setModel(Sistema.getInstance().mostrarListadoUsuarios());
                } catch (SQLException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_lblEliminarMouseClicked

