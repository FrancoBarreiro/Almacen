    private void lblCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblCrearMouseClicked
        String nombreUsuario = txtNuevo_Usuario.getText();
        String contrasenia = txtContrasenia.getText();
        String confirmContrasenia = txtConfirmarContrasenia.getText();
        String idEmpleado = txtIdEmpleado.getText();
        String tipoUsuario = null;

        int selecTipoUsuario = cboTipo_Usuario.getSelectedIndex();

        if (lblCrear.isEnabled()) {
            if (nombreUsuario.isEmpty() || contrasenia.isEmpty() || confirmContrasenia.isEmpty() || idEmpleado.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Verifique los campos obligatorios.");
            } else if (!txtContrasenia.getText().equals(txtConfirmarContrasenia.getText())) {
                JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden.");
            } else if (selecTipoUsuario == 0) {
                JOptionPane.showMessageDialog(this, "Seleccione el tipo de usuario...");
            } else if (selecTipoUsuario == 1) {
                tipoUsuario = "Adm";
                try {
                    if (Sistema.getInstance().crearUsuario(nombreUsuario, contrasenia, idEmpleado, tipoUsuario)) {
                        JOptionPane.showMessageDialog(this, "¡Usuario creado exitósamente!");
                    } else {
                        JOptionPane.showMessageDialog(this, "Error! Posibles causas: 1) El usuario ya existe. 2) El empleado ya tiene usuario creado. 3) El empleado no existe.");
                    }
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (selecTipoUsuario == 2) {
                tipoUsuario = "Ven";
                try {
                    if (Sistema.getInstance().crearUsuario(nombreUsuario, contrasenia, idEmpleado, tipoUsuario)) {
                        JOptionPane.showMessageDialog(this, "¡Usuario creado exitósamente!");
                    } else {
                        JOptionPane.showMessageDialog(this, "Error! Posibles causas: 1) El usuario ya existe. 2) El empleado ya tiene usuario creado. 3) El empleado no existe.");
                    }
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            try {
                tbeListado_Usuarios.setModel(Sistema.getInstance().mostrarListadoUsuarios());
            } catch (SQLException ex) {
                Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_lblCrearMouseClicked

