    private void lblBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBuscarMouseClicked
        String cedula = null;
        if (txtBuscador.getText().isEmpty() || txtBuscador.getText().equals("Ingresar C.I empleado...")) {
            JOptionPane.showMessageDialog(this, "Ingrese el número de C.I para buscar empleado...");
        } else if (txtBuscador.getText().length() == 8) {
            try {
                int cedulaNumerica = Integer.parseInt(txtBuscador.getText());
                cedula = String.valueOf(cedulaNumerica);
                lblIdEmpleado.setText("" + Sistema.getInstance().buscarEmpleadoPorCedula(cedula));
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "El campo debe ser numérico");
            } catch (SQLException ex) {
                Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "C.I inválida. Verifique nuevamente...");
        }
    }//GEN-LAST:event_lblBuscarMouseClicked

