    private void lblEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEliminarMouseClicked
        int filaSeleccionada = tbeListado_Productos.getSelectedRow();
        String id = null;
        String nombre = null;
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione el producto que desea eliminar...");
        } else {
            id = String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 0));
            nombre = String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 1));
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Desea eliminar el producto: " + nombre + "?", "Confirmación de eliminación...", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirmacion == 0) {
                try {
                    if (Sistema.getInstance().eliminarProducto(id)) {
                        JOptionPane.showMessageDialog(this, "Se ha elmininado correctamente.");
                    } else {
                        JOptionPane.showMessageDialog(this, "Se ha producido un error, intente nuevamente.");
                    }
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            try {
                tbeListado_Productos.setModel(Sistema.getInstance().mostrarListadoProductos());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                txtId.setText(Sistema.getInstance().mostrarSiguienteIdProducto());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_lblEliminarMouseClicked

