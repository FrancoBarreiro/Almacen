    private void lblModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblModificarMouseClicked

        int filaSeleccionada = tbeListado_Productos.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione el producto que desea modificar...");
        } else {
            lblActualizar.setEnabled(true);
            btnActualizar.setBackground(new Color(255, 77, 123));
            lblRegistrar.setEnabled(false);
            btnRegistrar.setBackground(new Color(255, 176, 199));
            txtId.setText(String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 0)));
            txtNombre.setText(String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 1)));

            if (String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 2)).equalsIgnoreCase("FRU")) {
                cboCategoria.setSelectedIndex(1);
            } else if (String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 2)).equalsIgnoreCase("VER")) {
                cboCategoria.setSelectedIndex(2);
            }
            tbeListado_Productos.setEnabled(false);
        }
    }//GEN-LAST:event_lblModificarMouseClicked

