    private void lblConfirmarVentaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblConfirmarVentaMouseClicked
        SimpleDateFormat formatoFecha = new SimpleDateFormat("YYYY-MM-dd");
        String fecha = "";
        String idVenta = null;
        String idProducto = null;
        double precioKg = 0;
        double cantidad = 0;

        if (tbeDetalleVenta.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "La lista no puede estar vacía");
        } else {
            try {
                fecha = formatoFecha.format(jDateFecha.getDate());
                Producto productos[] = new Producto[tbeDetalleVenta.getRowCount()];
                if (tbeDetalleVenta.getRowCount() > 0) {
                    for (int i = 0; i < tbeDetalleVenta.getRowCount(); i++) {
                        idProducto = (String) tbeDetalleVenta.getValueAt(i, 0);
                        cantidad = Double.parseDouble((String) tbeDetalleVenta.getValueAt(i, 3));
                        precioKg = (Double.parseDouble((String) tbeDetalleVenta.getValueAt(i, 2)));
                        Producto producto = new Producto("", "", idProducto, cantidad, precioKg);
                        productos[i] = producto;
                    }
                }
                idVenta = txtCodigo.getText();
                try {
                    if (Sistema.getInstance().registrarVenta(productos, fecha, idVenta)) {
                        tbeListado_Productos.setModel(Sistema.getInstance().mostrarListadoProductosVenta());
                        JOptionPane.showMessageDialog(this, "Se realizó la venta.");
                    } else {
                        JOptionPane.showMessageDialog(this, "Ocurrió un error, intente nuevamente.");
                        tbeListado_Productos.setModel(Sistema.getInstance().mostrarListadoProductosVenta());
                    }
                } catch (ClassNotFoundException ex) {
                    java.util.logging.Logger.getLogger(Ventas.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Ventas.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    java.util.logging.Logger.getLogger(Ventas.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                }
            } catch (NullPointerException e) {
                JOptionPane.showMessageDialog(this, "Seleccione una fecha");
            }
        }
    }//GEN-LAST:event_lblConfirmarVentaMouseClicked

