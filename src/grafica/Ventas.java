    private void lblAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAgregarMouseClicked

        String idProducto = null;
        String producto = null;
        double importe = 0;
        double totalProducto = 0;
        double cantidad = 0;
        double sumaTotal = 0;
        double gananciaTotal = 0;

        if (!txtCantidad.getText().matches("[0-9]+.?[0-9]+") || txtCantidad.getText().isEmpty() || Double.parseDouble(txtCantidad.getText()) <= 0) {
            JOptionPane.showMessageDialog(this, "Cantidad inválida.");
        } else if (Double.parseDouble(txtCantidad.getText()) > Double.parseDouble(String.valueOf(tbeListado_Productos.getValueAt(tbeListado_Productos.getSelectedRow(), 3)))) {
            JOptionPane.showMessageDialog(this, "Stock insuficiente.");
        } else if (txtProducto.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Seleccione un producto.");
        } else {
            int filaSeleccionada = tbeListado_Productos.getSelectedRow();
            idProducto = String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 0));
            producto = String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 1));
            cantidad = Double.parseDouble(txtCantidad.getText());
            importe = Double.parseDouble(String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 2))) * cantidad;
            double sumar = 0;
            String agregarProducto[] = new String[5];
            agregarProducto[0] = idProducto;
            agregarProducto[1] = producto;
            agregarProducto[2] = String.valueOf(tbeListado_Productos.getValueAt(tbeListado_Productos.getSelectedRow(), 2));
            agregarProducto[3] = txtCantidad.getText();
            agregarProducto[4] = String.valueOf(importe);
            modelo.addRow(agregarProducto);

            if (tbeDetalleVenta.getRowCount() > 0) {
                for (int i = 0; i < tbeDetalleVenta.getRowCount(); i++) {
                    sumar = Double.parseDouble((String) tbeDetalleVenta.getValueAt(i, 4));
                    sumaTotal += sumar;
                }
            }
            lblTotal.setText("Total: $" + sumaTotal);
            tbeListado_Productos.setEnabled(true);
        }
    }//GEN-LAST:event_lblAgregarMouseClicked

