    private void lblBuscarFrutaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBuscarFrutaMouseClicked
        String buscar = null;
        if (txtBuscadorFrutas.getText().isEmpty() || txtBuscadorFrutas.getText().equals("Buscar por nombre...")) {
            JOptionPane.showMessageDialog(this, "Ingrese el producto que desea buscar.");
        } else {
            buscar = "%" + txtBuscadorFrutas.getText() + "%";
            try {
                tbeStockFrutas.setModel(Sistema.getInstance().buscarFruta(buscar));
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Almacen.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Almacen.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Almacen.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_lblBuscarFrutaMouseClicked

