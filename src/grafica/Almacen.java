    private void lblConfirmarCompraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblConfirmarCompraMouseClicked
        SimpleDateFormat formatoFecha = new SimpleDateFormat("YYYY-MM-dd");
        String fecha = "";
        String numeroFactura = null;
        String idSuministrador = null;
        String idProducto = null;
        double precioKg = 0;
        double cantidad = 0;

        if (tbeDetalleCompra.getRowCount() > 0) {
            try {
                fecha = formatoFecha.format(jDateFecha.getDate());
            } catch (NullPointerException e) {
                JOptionPane.showMessageDialog(this, "Seleccione una fecha");
            }
        }
        if (tbeDetalleCompra.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "La lista no puede estar vacía");
        } else if (txtNumeroFactura.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el número de Factura/Boleta");
        } else if (cboSuministrador.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un suministrador");
        } else {
            Producto productos[] = new Producto[tbeDetalleCompra.getRowCount()];
            if (tbeDetalleCompra.getRowCount() > 0) {
                for (int i = 0; i < tbeDetalleCompra.getRowCount(); i++) {
                    idProducto = (String) tbeDetalleCompra.getValueAt(i, 0);
                    cantidad = Double.parseDouble((String) tbeDetalleCompra.getValueAt(i, 2));
                    precioKg = (Double.parseDouble((String) tbeDetalleCompra.getValueAt(i, 3))) / cantidad;
                    Producto producto = new Producto("", "", idProducto, cantidad, precioKg);
                    productos[i] = producto;
                }
            }
            numeroFactura = txtNumeroFactura.getText();
            idSuministrador = String.valueOf(cboSuministrador.getItemAt(cboSuministrador.getSelectedIndex()));
            String separarSuministrador[] = idSuministrador.split(" - ", 2);
            idSuministrador = separarSuministrador[0];
            try {
                if (Sistema.getInstance().ingresarCompra(productos, fecha, idSuministrador, numeroFactura)) {
                    JOptionPane.showMessageDialog(this, "Se registró la compra.");
                } else {
                    JOptionPane.showMessageDialog(this, "Ha ocurrido un error, intente nuevamente.");
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Almacen.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Almacen.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Almacen.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_lblConfirmarCompraMouseClicked

