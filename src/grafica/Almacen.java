    private void lblAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAgregarMouseClicked

        String idProducto = null;
        String producto = null;
        double precio = 0;
        double totalProducto = 0;
        int cantidad = 0;
        double sumaTotal = 0;

        if (!txtPrecioKg.getText().matches("[0-9]+.?[0-9]+") || txtPrecioKg.getText().isEmpty() || Double.parseDouble(txtPrecioKg.getText()) <= 0) {
            JOptionPane.showMessageDialog(this, "Digite un precio válido");
        } else if (!txtCantidad.getText().matches("[0-9]+") || txtPrecioKg.getText().isEmpty() || Double.parseDouble(txtCantidad.getText()) <= 0) {
            JOptionPane.showMessageDialog(this, "Digite una cantidad válida");
        } else {
            int filaSeleccionada = tbeListado_Productos.getSelectedRow();
            idProducto = String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 0));
            producto = String.valueOf(tbeListado_Productos.getValueAt(filaSeleccionada, 1));
            cantidad = Integer.parseInt(txtCantidad.getText());
            precio = Double.parseDouble(txtPrecioKg.getText());
            totalProducto = precio * cantidad;
            double sumar = 0;
            String agregarProducto[] = new String[4];
            agregarProducto[0] = idProducto;
            agregarProducto[1] = producto;
            agregarProducto[2] = txtCantidad.getText();
            agregarProducto[3] = String.valueOf(totalProducto);
            modelo.addRow(agregarProducto);

            if (tbeDetalleCompra.getRowCount() > 0) {
                for (int i = 0; i < tbeDetalleCompra.getRowCount(); i++) {
                    sumar = Double.parseDouble((String) tbeDetalleCompra.getValueAt(i, 3));
                    sumaTotal += sumar;
                }
            }
            lblTotal.setText("Total: $" + sumaTotal);
            tbeListado_Productos.setEnabled(true);
        }
    }//GEN-LAST:event_lblAgregarMouseClicked

