    private void lblEliminarProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEliminarProductoMouseClicked
        DefaultTableModel modelo = (DefaultTableModel) tbeDetalleCompra.getModel();

        int filaSeleccionada = tbeDetalleCompra.getSelectedRow();
        double sumaTotal = 0.0;
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione el producto que desea eliminar de la lista...");
        } else {
            modelo.removeRow(filaSeleccionada);
            if (tbeDetalleCompra.getRowCount() > 0) {
                for (int i = 0; i < tbeDetalleCompra.getRowCount(); i++) {
                    double sumar = Double.parseDouble((String) tbeDetalleCompra.getValueAt(i, 3));
                    sumaTotal += sumar;
                }
            }
            lblTotal.setText("Total: $" + sumaTotal);
        }

    }//GEN-LAST:event_lblEliminarProductoMouseClicked

