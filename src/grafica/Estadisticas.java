    private void lblBuscarVentasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBuscarVentasMouseClicked
        SimpleDateFormat formatoFecha = new SimpleDateFormat("YYYY-MM-dd");
        String fechaDesde = "";
        String fechaHasta = ""; 
        Date fecha1 = null;
        Date fecha2 = null;
        try {
            fechaDesde = formatoFecha.format(jDateDesde.getDate());
            fechaHasta = formatoFecha.format(jDateHasta.getDate());
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "Seleccione una fecha");
        }
        
        try {
            fecha1 = formatoFecha.parse(fechaDesde);
        } catch (ParseException ex) {
            Logger.getLogger(Estadisticas.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            fecha2 = formatoFecha.parse(fechaHasta);
        } catch (ParseException ex) {
            Logger.getLogger(Estadisticas.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(fecha1.before(fecha2) || fecha1.equals(fecha2)) {
            try {
                tbeVentasPorPeriodo.setModel(Sistema.getInstance().verVentasPorPeriodo(fechaDesde, fechaHasta));
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Estadisticas.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Estadisticas.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Estadisticas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_lblBuscarVentasMouseClicked

